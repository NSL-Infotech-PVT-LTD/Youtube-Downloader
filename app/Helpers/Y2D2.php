    public static function youtube_url_to_id($url) {
        $values = '';
        if (preg_match('/youtube\.com\/watch\?v=([^\&\?\/]+)/', $url, $id)) {
            $values = $id[1];
        } else if (preg_match('/youtube\.com\/embed\/([^\&\?\/]+)/', $url, $id)) {
            $values = $id[1];
        } else if (preg_match('/youtube\.com\/v\/([^\&\?\/]+)/', $url, $id)) {
            $values = $id[1];
        } else if (preg_match('/youtu\.be\/([^\&\?\/]+)/', $url, $id)) {
            $values = $id[1];
        } else if (preg_match('/youtube\.com\/verify_age\?next_url=\/watch%3Fv%3D([^\&\?\/]+)/', $url, $id)) {
            $values = $id[1];
        }
        return $values;
    }

    public static function size_to_string($bytes) {
        if ($bytes >= 1073741824) {
            $bytes = number_format($bytes / 1073741824, 2) . ' GB';
        } elseif ($bytes >= 1048576) {
            $bytes = number_format($bytes / 1048576, 2) . ' MB';
        } elseif ($bytes >= 1024) {
            $bytes = number_format($bytes / 1024, 2) . ' KB';
        } elseif ($bytes > 1) {
            $bytes = $bytes . ' bytes';
        } elseif ($bytes == 1) {
            $bytes = $bytes . ' byte';
        } else {
            $bytes = 'N/A';
        }
        return $bytes;
    }

    public static function mp3_size($kbps, $duration) {
        if (isset($kbps) && isset($duration)) {
            $size = $kbps * $duration / 8 * 1000;
            return size_to_string($size);
        }
        return false;
    }

    public static function destroy($dir, $life = 3600) {
        if (is_dir($dir)) {
            $objects = scandir($dir);
            foreach ($objects as $object) {
                if ($object != "." && $object != "..") {
                    if (filetype($dir . "/" . $object) == "dir")
                        destroy($dir . "/" . $object, $life);
                    else if (time() - filemtime($dir . "/" . $object) > $life) {
                        @unlink($dir . "/" . $object);
                        echo $dir . "/" . $object . ' - Deleted<br>';
                    }
                }
            }
//        rmdir($dir);
//        echo $dir . ' - Deleted<br>';
        }
    }

    public static function remoteFile_size($url) {
        $data = get_headers($url, true);
        if (isset($data['Content-Length']))
            return (int) $data['Content-Length'];
    }

